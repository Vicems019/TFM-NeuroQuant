import torch
import torch.nn as nn

class LSTMModel(nn.Module):
    def __init__(self, input_size=20, hidden_size=256, num_layers=2,
                 output_size=4, dropout=0.2,
                 mlp_hidden=64, head_hidden=32):
        super().__init__()

        self.input_proj = nn.Sequential(
            nn.Linear(input_size, hidden_size),   # input_proj.0
            nn.LayerNorm(hidden_size)             # input_proj.1
        )

        self.lstm = nn.LSTM(
            input_size  = hidden_size,
            hidden_size = hidden_size,
            num_layers  = num_layers,
            batch_first = True,
            dropout     = dropout if num_layers > 1 else 0.0
        )

        self.attn = nn.Linear(hidden_size, 1)
        self.norm = nn.LayerNorm(hidden_size)

        # MLP: hidden_size → mlp_hidden → head_hidden
        self.mlp = nn.Sequential(
            nn.Linear(hidden_size, mlp_hidden),   # mlp.0
            nn.LayerNorm(mlp_hidden),             # mlp.1
            nn.ReLU(),                            # mlp.2 (sin parámetros)
            nn.Dropout(dropout),                  # mlp.3 (sin parámetros)
            nn.Linear(mlp_hidden, head_hidden),   # mlp.4
        )

        self.head = nn.Linear(head_hidden, output_size)

    def forward(self, x):
        x       = self.input_proj(x)
        out, _  = self.lstm(x)
        attn_w  = torch.softmax(self.attn(out), dim=1)
        context = (attn_w * out).sum(dim=1)
        context = self.norm(context)
        context = self.mlp(context)
        return self.head(context)   # → (batch, 4)

