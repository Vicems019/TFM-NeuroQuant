@api.post("/predict")
def predict(req: PredictRequest):
  try:
      symbol = req.symbol.upper()
      if f'{symbol}_LSTM' not in MODELS:
          raise HTTPException(404, f"Modelo {symbol} no disponible")

      t0     = time.perf_counter()
      X      = np.array(req.X_input, dtype=np.float32)   # (24, 20) raw
      X_norm = scalers[symbol].transform(X)               # (24, 20) escalado

      lstm_model = MODELS[f'{symbol}_LSTM']
      sac_model  = MODELS[f'{symbol}_SAC']

      # ── Precio base ───────────────────────────────────────────────────
      # Usar precio live del cliente si llega, si no el último close de X
      current_price = float(req.current_price) if req.current_price \
                      else float(X[-1, 0])

      # ── LSTM — predicción principal ───────────────────────────────────
      tensor = torch.FloatTensor(X_norm).unsqueeze(0).to(DEVICE)
      with torch.no_grad():
          preds_norm = lstm_model(tensor)[0].tolist()   # 4 retornos normalizados

      preds_real = reg_scalers[symbol].inverse_transform(
          np.array(preds_norm).reshape(1, -1)
      ).flatten().tolist()

      price_1h = round(current_price * (1 + preds_real[0]), 2)
      price_2h = round(current_price * (1 + preds_real[1]), 2)
      price_3h = round(current_price * (1 + preds_real[2]), 2)
      price_4h = round(current_price * (1 + preds_real[3]), 2)

      # ── Monte Carlo Dropout ───────────────────────────────────────────
      scale_1h  = float(reg_scalers[symbol].scale_[0])
      center_1h = float(reg_scalers[symbol].center_[0])

      lstm_model.train()
      mc_preds = []
      with torch.no_grad():
          for _ in range(50):
              mc_preds.append(lstm_model(tensor)[0][0].item())
      lstm_model.eval()

      mc_min_ret = float(np.percentile(mc_preds,  5)) * scale_1h + center_1h
      mc_max_ret = float(np.percentile(mc_preds, 95)) * scale_1h + center_1h
      price_min  = round(current_price * (1 + mc_min_ret), 2)
      price_max  = round(current_price * (1 + mc_max_ret), 2)

      # ── SAC — decisión ────────────────────────────────────────────────
      X_sac_norm = np.clip(
          (X - sac_means[symbol]) / sac_stds[symbol],
          -5.0, 5.0
      ).astype(np.float32)

      state_rl       = np.zeros(643, dtype=np.float32)
      state_rl[160:640] = X_sac_norm.flatten()
      state_rl[640]  = 0.0   # position: sin posición abierta
      state_rl[641]  = 0.0   # PnL: neutro
      state_rl[642]  = 1.0   # capital: completo disponible
      # ⚠ slots 480:640 quedan en 0 hasta tener feat_mean/feat_std nuevos

      action, _  = sac_model.predict(state_rl, deterministic=True)
      raw_action = float(action)
      abs_action = abs(raw_action)

      if raw_action < -0.33:
          sac_decision = "VENDER"
          probs = [round(0.33 + abs_action * 0.34, 2), 0.33,
                   round(0.34 - abs_action * 0.34, 2)]
      elif raw_action > 0.33:
          sac_decision = "COMPRAR"
          probs = [round(0.34 - abs_action * 0.34, 2), 0.33,
                   round(0.33 + abs_action * 0.34, 2)]
      else:
          sac_decision = "MANTENER"
          probs = [round(0.33 - abs_action * 0.1, 2),
                   round(0.34 + abs_action * 0.2, 2),
                   round(0.33 - abs_action * 0.1, 2)]

      # ── Árbitro LSTM vs SAC — señal primaria: H4 ─────────────────────
      MIN_CONFIDENCE = 0.45   # umbral mínimo para operar

      lstm_direction = 1 if preds_real[3] > 0 else -1
      sac_direction  = (1 if raw_action > 0.33
                  else (-1 if raw_action < -0.33 else 0))

      if lstm_direction == sac_direction:
        final_decision   = sac_decision
        final_confidence = round(max(probs), 4)
        signal_agreement = True

      elif sac_direction == 0:

        final_confidence = round(abs(preds_real[3]) * 100, 4)
        if final_confidence >= MIN_CONFIDENCE:
          final_decision   = "COMPRAR" if lstm_direction > 0 else "VENDER"
          signal_agreement = True
        else:
          final_decision   = "MANTENER"
          signal_agreement = True   # no es contradicción, es señal débil
          probs            = [0.33, 0.34, 0.33]

      else:
        final_decision   = "MANTENER"
        final_confidence = 0.33
        probs            = [0.33, 0.34, 0.33]
        signal_agreement = False


      # ── Métricas sobre precios reales ─────────────────────────────────
      prices = X[:, 0]
      actual_returns = np.diff(prices) / (prices[:-1] + 1e-8)   # (23,)

      preds_hist = []
      with torch.no_grad():
          for i in range(4, len(prices)):
              t_slice = torch.FloatTensor(
                  X_norm[i-4:i]).unsqueeze(0).to(DEVICE)
              pred_n  = lstm_model(t_slice)[0][0].item()
              preds_hist.append(pred_n * scale_1h + center_1h)

      actual_h = actual_returns[3:]
      ph       = np.array(preds_hist)

      prices_base   = prices[3:23]
      prices_actual = prices[4:24]
      prices_pred   = prices_base * (1 + ph)

      rmse   = float(np.sqrt(np.mean((prices_actual - prices_pred) ** 2)))
      mae    = float(np.mean(np.abs(prices_actual - prices_pred)))
      mape   = float(np.mean(
          np.abs((prices_actual - prices_pred) / prices_actual)) * 100)
      rets   = np.diff(ph) / (np.abs(ph[:-1]) + 1e-8)
      sharpe = float((rets.mean() / (rets.std() + 1e-8)) * np.sqrt(252))

      pred_dirs   = np.sign(ph)
      actual_dirs = np.sign(actual_h)
      window_diracc = float(np.mean(pred_dirs == actual_dirs))

      # ── SHAP ──────────────────────────────────────────────────────────
      base_seq = X_norm.copy()
      def lstm_predict_flat(x_flat):
          results = []
          for row in x_flat:
              seq = base_seq.copy()
              seq[-1] = row
              t = torch.FloatTensor(seq).unsqueeze(0).to(DEVICE)
              with torch.no_grad():
                  results.append(lstm_model(t)[0][0].item())
          return np.array(results)

      background = X_norm[-5:]
      explainer  = shap.KernelExplainer(lstm_predict_flat, background)
      shap_vals  = explainer.shap_values(
          X_norm[-1:], nsamples=100, silent=True)

      latency = round((time.perf_counter() - t0) * 1000, 1)

      return {
          "symbol": symbol,
          "lstm": {
              "predicted_price": price_1h,
              "1h":              price_1h,
              "2h":              price_2h,
              "3h":              price_3h,
              "4h":              price_4h,
              "min":             price_min,
              "max":             price_max,
          },
          "rl": {
              "decision":         final_decision,
              "action_probs":     probs,
              "confidence":       final_confidence,
              "raw_action":       round(raw_action, 4),
              "signal_agreement": signal_agreement,
          },
          "metrics": {
              "rmse":          round(rmse,   2),
              "mae":           round(mae,    2),
              "mape":          round(mape,   4),
              "sharpe_window": round(sharpe, 4),
              "window_diracc": round(window_diracc, 4),
          },
          "direction_accuracy": WF_DIRACC.get(symbol, {}),
          "shap": {
              "feature_names": FEATURE_NAMES,
              "values":        shap_vals[0].tolist(),
              "base_value":    float(explainer.expected_value),
          },
          "latency_ms": latency,
      }

  except Exception as e:
      import traceback
      raise HTTPException(status_code=500, detail=traceback.format_exc())

